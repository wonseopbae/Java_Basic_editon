package Hello;

public class Ex2_6 {

	public static void main(String[] args) {
		int x = 100; //정수 integer를 저장할 변수 타입은 int로 한다.
		double pi = 3.14; // 실수를 저장 할 변수 타입은 double로 한다.
		char ch = 'a'; //문자 1개를 저장 할 변수 타입은 char로 한다.
		String str = "abc"; //문자 0 ~ n개를 저장 할 변수 타입을 String으로 한다.
		
		System.out.println(x);
		System.out.println(pi);
		System.out.println(ch);
		System.out.println(str);
	}

}
