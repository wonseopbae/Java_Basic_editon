package Hello;

public class Ex2_5 {

	public static void main(String[] args) {
		int x = 10; // 변수 x에 10을 저장한다.
		int y = 5; // 변수 y에 5를 저장한다.
		
		System.out.println(x+y); // 화면에 15가 출력된다.
		System.out.println(x-y); // 화면에 5가 출력된다.
		System.out.println(x*y); // 화면에 50이 출력된다.
		System.out.println(x/y); // 화면에 2가 출력된다.
	}

}
